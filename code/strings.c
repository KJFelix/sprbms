unsigned char strlen(unsigned char *str)
{
	unsigned char len = 0;
	for(;*str != 0;str++)
	{
		len++;
	}
	return len;
}

void strcpy(unsigned char *dest, unsigned char *src)
{
	while(*src != 0)
	{
		*(dest++) = *(src++);
	}
}
